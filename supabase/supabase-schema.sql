-- Helper function to check if user is admin/manager
create or replace function public.is_admin()
returns boolean as $$
  select exists (
    select 1 from profiles
    where id = auth.uid() and role in ('admin', 'manager')
  );
$$ language sql security definer;

-- Profiles: users can read their own, admins can read all
create policy "Users can view own profile"
  on profiles for select
  using (auth.uid() = id);

create policy "Admins can view all profiles"
  on profiles for select
  using (public.is_admin());

create policy "Users can update own profile"
  on profiles for update
  using (auth.uid() = id);

create policy "Users can insert own profile"
  on profiles for insert
  with check (auth.uid() = id);

-- Products: everyone can read active products, admins can manage
create policy "Anyone can view active products"
  on products for select
  using (status = 'activo' or status = 'agotado');

create policy "Admins can view all products"
  on products for select
  using (public.is_admin());

create policy "Admins can insert products"
  on products for insert
  with check (public.is_admin());

create policy "Admins can update products"
  on products for update
  using (public.is_admin());

create policy "Admins can delete products"
  on products for delete
  using (public.is_admin());

-- Orders: users can see their own, admins can see all
create policy "Users can view own orders"
  on orders for select
  using (user_id = auth.uid());

create policy "Admins can view all orders"
  on orders for select
  using (public.is_admin());

create policy "Anyone can create orders"
  on orders for insert
  with check (true);

create policy "Admins can update orders"
  on orders for update
  using (public.is_admin());

-- Appointments: users can see their own, admins can see all
create policy "Users can view own appointments"
  on appointments for select
  using (user_id = auth.uid());

create policy "Admins can view all appointments"
  on appointments for select
  using (public.is_admin());

create policy "Anyone can create appointments"
  on appointments for insert
  with check (true);

create policy "Admins can update appointments"
  on appointments for update
  using (public.is_admin());

-- Notifications: users can see their own + global (null user_id), admins can manage
create policy "Users can view own notifications"
  on notifications for select
  using (user_id = auth.uid() or user_id is null);

create policy "Admins can view all notifications"
  on notifications for select
  using (public.is_admin());

create policy "Admins can insert notifications"
  on notifications for insert
  with check (public.is_admin());

create policy "Admins can update notifications"
  on notifications for update
  using (public.is_admin());

create policy "Users can update own notifications"
  on notifications for update
  using (user_id = auth.uid());

-- Activities: admins can view all, system can insert
create policy "Admins can view activities"
  on activities for select
  using (public.is_admin());

create policy "System can insert activities"
  on activities for insert
  with check (true);

-- Settings: everyone can read, only admins can update
create policy "Anyone can view settings"
  on settings for select
  using (true);

create policy "Admins can update settings"
  on settings for all
  using (public.is_admin());

