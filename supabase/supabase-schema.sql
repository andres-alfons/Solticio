-- Enable UUID extension
create extension if not exists "uuid-ossp";

-- ============================================
-- USERS TABLE (extends Supabase auth.users)
-- ============================================
create table profiles (
  id uuid references auth.users(id) on delete cascade primary key,
  name text not null,
  email text not null,
  phone text default '',
  role text not null default 'client' check (role in ('client', 'staff', 'manager', 'admin')),
  avatar text default '',
  wishlist int[] default '{}',
  created_at timestamp with time zone default now(),
  updated_at timestamp with time zone default now(),
  last_login timestamp with time zone
);

-- ============================================
-- PRODUCTS TABLE
-- ============================================
create table products (
  id bigint generated by default as identity primary key,
  name text not null,
  slug text unique not null,
  category text not null,
  price numeric not null,
  stock int not null default 0,
  min_stock int not null default 3,
  description text default '',
  description_long text default '',
  image_url text default '',
  images text[] default '{}',
  colors text[] default '{}',
  sizes text[] default '{}',
  tags text[] default '{}',
  status text not null default 'activo' check (status in ('activo', 'inactivo', 'agotado')),
  discount numeric not null default 0,
  featured boolean not null default false,
  created_at timestamp with time zone default now(),
  views int not null default 0,
  sales int not null default 0
);

-- ============================================
-- ORDERS TABLE
-- ============================================
create table orders (
  id text primary key,
  user_id uuid references profiles(id) on delete set null,
  client_name text not null,
  client_email text not null,
  client_phone text default '',
  client_doc text default '',
  products jsonb not null,
  total numeric not null,
  status text not null default 'pendiente' check (status in ('pendiente', 'confirmado', 'en_produccion', 'enviado', 'entregado', 'cancelado')),
  payment text not null,
  address text not null,
  notes text default '',
  tracking text default '',
  carrier text default '',
  cancellation_reason text default '',
  cancellation_detail text default '',
  created_at timestamp with time zone default now(),
  updated_at timestamp with time zone default now()
);

-- ============================================
-- APPOINTMENTS TABLE
-- ============================================
create table appointments (
  id bigint generated by default as identity primary key,
  user_id uuid references profiles(id) on delete set null,
  client_name text not null,
  type text not null,
  date date not null,
  time text not null,
  status text not null default 'pendiente' check (status in ('pendiente', 'confirmada', 'cancelada', 'completada')),
  notes text default '',
  created_at timestamp with time zone default now()
);

-- ============================================
-- NOTIFICATIONS TABLE
-- ============================================
create table notifications (
  id bigint generated by default as identity primary key,
  user_id uuid references profiles(id) on delete cascade,
  type text not null,
  title text not null,
  message text not null,
  read boolean not null default false,
  link text default '',
  created_at timestamp with time zone default now()
);

-- ============================================
-- ACTIVITIES TABLE (Admin Activity Feed)
-- ============================================
create table activities (
  id bigint generated by default as identity primary key,
  user_id uuid references profiles(id) on delete set null,
  type text not null,
  text text not null,
  created_at timestamp with time zone default now()
);

-- ============================================
-- SETTINGS TABLE
-- ============================================
create table settings (
  key text primary key,
  value jsonb not null,
  updated_at timestamp with time zone default now()
);

