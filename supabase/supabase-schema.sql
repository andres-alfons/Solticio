-- Auto-create notification on low stock
create or replace function public.check_low_stock()
returns trigger as $$
begin
  if new.stock <= new.min_stock and new.stock > 0 then
    insert into public.notifications (user_id, type, title, message, link)
    values (
      null,
      'stock',
      'Stock bajo: ' || new.name,
      new.name || ' tiene solo ' || new.stock || ' unidades (mínimo: ' || new.min_stock || ')',
      '/admin/index.html?page=inventory'
    );
  elsif new.stock = 0 then
    insert into public.notifications (user_id, type, title, message, link)
    values (
      null,
      'stock',
      'Producto agotado: ' || new.name,
      new.name || ' se quedó sin stock',
      '/admin/index.html?page=inventory'
    );
  end if;
  return new;
end;
$$ language plpgsql security definer;

create trigger on_stock_change
  after update on products
  for each row
  when (old.stock > new.stock)
  execute procedure public.check_low_stock();

-- Increment product sales on order
create or replace function public.increment_product_sales()
returns trigger as $$
declare
  product_record jsonb;
begin
  if new.status = 'confirmado' or new.status = 'enviado' or new.status = 'entregado' then
    for product_record in select * from jsonb_array_elements(new.products)
    loop
      update products
      set sales = sales + (product_record->>'qty')::int
      where id = (product_record->>'id')::bigint;
    end loop;
  end if;
  return new;
end;
$$ language plpgsql security definer;

create trigger on_order_status_change
  after update on orders
  for each row
  when (old.status <> new.status and new.status in ('confirmado', 'enviado', 'entregado'))
  execute procedure public.increment_product_sales();

